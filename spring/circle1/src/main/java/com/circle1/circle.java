package com.circle1;

public class circle {


		String name;  
		int radius;  
	 
		  
		public circle(String name, int radius) {  
		     
		    this.name = name;  
		    this.radius = radius;  
		
		}  
		  
		public String toString(){  
		    return name+" "+radius+"  ";
		}  
		}  


